package controllers;
/**
 * Enum representing the duration categories for camps: 
 * SHORT - Represents a short duration camp (lesser than 5 days) | 
 * NORMAL - Represents a medium duration camp (greater than or equal to 5 days and lesser than or equal to 10 days) | 
 * LONG - Represents a long duration camp (greater than 10 days)
 */
public enum DURATION {
	SHORT, NORMAL, LONG
}
